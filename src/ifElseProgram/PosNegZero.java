package ifElseProgram;

public class PosNegZero {

	public static void main(String[] args) {
		
		int num = 0;
		
		if(num > 0)
		{
			System.out.println(num+" Positive Number");
		}
		else if(num < 0)
		{
			System.out.println(num+" Negative Number");
		}
		else 
		{
			System.out.println("The number is Zero");
		}

	}

}
