package ifElseProgram;

public class Max2Nos {

	public static void main(String[] args) {
		
		int num1 = 21;
		int num2 = 35;
		
		if(num1>num2)
		{
			//true part
			System.out.println("The greater number is: "+num1);
		}
		else 
		{
			//False Part
			System.out.println("The greater number is: "+num2);
		}

	}

}
