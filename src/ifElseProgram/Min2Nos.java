package ifElseProgram;

public class Min2Nos {

	public static void main(String[] args) {
		
		int num1 = 21;
		int num2 = 29;
		
		if(num1<num2)
		{
			//true part
			System.out.println("The smaller number is: "+num1);
		}
		else 
		{
			//False Part
			System.out.println("The smaller number is: "+num2);
		}

	}

}
